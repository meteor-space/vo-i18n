Country = Space.domain.ValueObject.extend('Country', {

  Constructor(data) {

    // Allow to provide another object with country attribute as param
    let country = (data && data.code) ? data.code : data;

    if(!Country.isValidCountryCode(country)) {
      throw new Error(Country.ERRORS.invalidCountryCode(country));
    }

    this.code = country;
    Object.freeze(this);
  },

  // Defines the EJSON fields that are automatically serialized
  fields() {
    return {
      code: String
    };
  },

  isEuropean() {
    return Country.isEuropean(this.code);
  },

  toString() {
    return this.code;
  }

});

Country.ERRORS = {
  invalidCountryCode(code) {
    return `Invalid country code '${code}'`;
  }
};

Country.isEuropean = function(code) {
  return Country.EUROPEAN_COUNTRIES.indexOf(code) > -1;
};

Country.isValidCountryCode = function(code) {
  return Country.ISO_COUNTRY_CODES.indexOf(code) > -1;
};

Country.getAll = function() {
  return Country.ISO_COUNTRY_CODES.slice();
};

Country.getEuropean = function() {
  return Country.EUROPEAN_COUNTRIES.slice();
};

